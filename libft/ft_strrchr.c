#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	while (*s)
	{
		s++;
	}
	while (*s - 1)
	{
		if (*s == c)
			return ((char *)s);
		s--;
	}
	if (*s == '\0' && c == '\0')
		return ((char *)s);
	return (NULL);
}